<h3><a name="section5">Inbox</a></h3>

<br> 

Now <b>this is personal</b>. This is your own, private Inbox on DP. 
It's not for email, it's for <b>Private Messages</b> (PMs) between you and other DP-ers. Nobody else reads your mail. 
If you have new messages waiting, next to the <b>Inbox</b> link you will see <b>(2 unread)</b> or however many it is.<br>

<br>

When you click on that link, and go to your Inbox, you see any messages waiting. 
As with email, you can <b>read</b>, <b>reply</b> (<b>quoting</b> or not), <b>save</b> or <b>delete</b> messages. <br>

<br>

If you know someone's DP username, you can type that into the <b>To:</b> field. 
If you aren't sure about the username, you can click on the <b>new post</b> link next to the 
<b>Distributed Proofreaders Forum Index</b> and then you can <b>search</b> for the username. <br>

<br>

For example, you might remember that someone called themselves Froggy something, but you're not sure. 
Putting "frog*" into the search may turn up Frogfish, Bigfrog and Froggy2. 
If the last one is the one you remember, you pick that. Handy, really. 
The * is a wildcard, and matches any string of letters.<br>

<br>

Helpful people at DP will send you feedback about your proofreading, to help you settle in, 
and those early messages will come to your <b>Inbox</b>. 

