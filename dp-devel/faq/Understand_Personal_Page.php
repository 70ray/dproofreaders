<h2><a name="section15">Books for Everyone</a></h2>

<br>

Here is (we hope) a long and interesting list of books. 
They are coloured according to themes (could be your favourite!).<br><br>

They are listed in the order they came into this queue, except for <b>Beginners'</b> books, which are listed first.<br>



<br>

The top list of books is those which have come straight from the Content Providers' steaming scanners:<b> Round One.</b> 
They may be full of errors, or they may not, according to the <i>scanner</i> (expensive? good software?), 
the state of the <i>original</i> book (faded? damaged?) and the <i>complexity</i> of the text 
(strange print? lots of footnotes? multiple columns?).


They are rated from <b>Beginners Only</b> (for very new proofers only) through <b>Easy</b> to <b>Hard</b> 
with helpful comments. 
<b>Easy</b> books are usually straight text, novels or non-fiction, very few or no footnotes, no hassles.
If a book doesn't show a difficulty, it's "average".<br>

<br>

It's up to you where you feel comfortable proofreading. 
Some people stay in one level or type of text, others dart around like dragonflies, flitting and sipping, 
some suffer from acute masochism and specialize in heavy footnoting or strange markup. 
Once you've settled in and done some Beginners' and Easy texts, you can take your pick.<br>

<br>

You can see the books' <b>title</b>, the <b>author</b>, the <b>language</b> the book is written in 
(and we try to have as great a variety as possible: 
please note your language skills in <a href="http://www.pgdp.net/phpBB2/viewtopic.php?t=2384">this post</a> in the Forums), 
the <b>genre</b> (novel? history? great dog story?), 
the <b>project manager</b> (who is looking after this book: good person to ask questions about it), 
how many <b>pages available</b> for proofreading right now (or as close to right now as the database gets) 
and the <b>total pages</b> in that book.<br>

<br>

When you click on the <b>title</b> of a book, the <b>proofreading interface</b> pops up in front of you 
like something out of a trap-door (but friendly!). 
You can stop change your mind and <b>stop proofreading</b>, 
<b>save</b> proofed pages and <b>go on</b> proofreading, start and stop whenever suits you. 
This is <i>your DP.</i><br>

<br>

Can you see a second list of books (<b>Second Round</b>)? 
Again, we're trying not to overload you with detail, and will introduce this list a bit later on, after 50 pages proofed. 
The second list of books is those which have already been through Round One. 
But they should be perfect now? I hear you protest. Um, yeah. 
That would be nice, but the <a href="http://www.pgdp.net/phpBB2/viewtopic.php?t=1563">scannos</a> 
(sneaky things the scanner does, e. g. l for 1 and 0 for O) do tend to slip past us. 
We've found by experience that what one person misses 
(and the better the reader you are, the more likely your eye is to correct as it reads, and thus miss the errors!), 
another will often find. 
Thus, we have two goes at each book. 
<b>Round Two</b> books are listed in alphabetical order, making it just a little easier for you to find a favourite book, 
or one that's recommended to you, later on.

