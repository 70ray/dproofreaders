<h3><a name="section4">Forums</a></h3>

<br>

This is your personal link into the <b>Forums</b>. When you click on this, you enter the Forums <i>as you</i>. 
Any posts or replies that you make will come from your username.  
When you log into DP, you are also logged into the Forums.<br>



<br>

The Forums are where people ask questions (aha!), have discussions and generally share ideas. 
Clicking on this link will take you to the <b>Forum Index</b>. 
This has grown quite a bit, but by scrolling down you can see that we try to cover everything people might want to discuss. 
You're welcome to enter any forum, but you may find the <b>Beginners' Forums</b> at the top, most useful to begin with. 
Check there first to find if your question has been asked and answered already; 
if not, post it and you'll hear back rsn (real soon now :) ).<br>

<br>

If you've ever used a Forum or Bulletin Board System (BBS) before, you know how if works, but in any case, it's not complicated. 
Someone has an idea, e.g. I might think that the whole Personal Page should be dark green. 
I hit <b>"new post"</b> to open a new topic (idea).  
Just like when writing an email, I fill in the <b>subject</b>: "How about a dark green Personal Page?" 
and then write what I want to say in the <b>body</b> of the post. 
I can click on <b>smilies</b> to illustrate my feelings about what I write. 
I can <b>preview</b> my post, and check that it looks OK, and then <b>submit</b> the post, 
or I can just <b>submit</b> it straight away. 
In any case, I can always <b>edit</b> my post afterwards if I want.<br>

<br>

[Please note that in some of the forums (e.g. the Project forum created for each book proofed) you can't create a New Topic, only Reply. 
This helps keep things on track.]<br>

<br>

Other people read my post, and I get replies, 
saying "You goof, if the whole page were dark green, we wouldn't be able to see the menu-bar" and other constructive things. 
I can write back, saying "So change the colour of the menu-bar," and as you can see, 
a discussion has been started which anybody on DP can join in. 
Each topic grows as people <b>reply</b> to it, and forums grow and change as people add new topics. 
It's a lot of fun, very useful and informative, and it's an important part of DP.<br>

<br>

We'll talk more about forums in a later page, but those are the basics, and you're very welcome to contribute! 
More also about the <b>Teams</b> forums in particular, later in <i>this</i> page.<br>

