<h1>The Distributed Proofreaders Foundation</h1>

<p>
This document provides some basic facts about the Distributed
Proofreaders Foundation (DPF). DPF is the not-for-profit corporation
that receives and processes donations to Distributed Proofeaders, and
seeks fundraising opportunities.
</p>

<h2>Mission:</h2>

<p>
The Distributed Proofreaders Foundation has been formed to support the
Distributed Proofreaders (DP) website (www.pgdp.net) which has existed,
in one form or another, since about October, 2000. DP volunteers
transcribe books and periodicals that are in the public domain into
electronic texts using software that has been developed by DP
volunteers. These electronic texts are freely distributed for the
benefit of anyone who can use them. 
</p>
<p>
While currently DP only transcribes text into ebooks, related activities
that might be included on the existing website in the future, or
supported by DPF in some fashion, include transcription of musical
notation, cooperative translation, and public access to the scanned
material produced by DP volunteers. In this sense, DPF's mission goes
beyond simple support of the DP website to the wider goal of
transcribing, into digital form, freely distributable material that is
currently only available on paper.
</p>

