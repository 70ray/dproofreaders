<h2>Tax Status:</h2>
<p>
DPF applied for tax exempt status by the IRS under section
501(c)(3) of the US Internal Revenue Code on July 31, 2006. 
</p>

<h2>Donations:</h2>

<p>
Our understanding is that now that DPF has made the application for
501(c)(3) status, donations are tax-deductible, with that status being
removed retroactively if the application is turned down. We believe that
DPF should have no trouble qualifying. All of this only matters to
donors who file US tax returns.
</p>
<p>
The PayPal donation button on the PGDP website directs donations to DPF.
Donations made by check should be made payable to "Distributed
Proofreaders Foundation" and mailed to the above address. DPF also
gladly accepts donations of stock.

</p>
<p>
DPF will report on its finances regularly (at least yearly, more often
if it seems appropriate) to the volunteers. 
</p>

<h2>Use of Donations:</h2>

<p>
Money donated to DPF will be used to support this website, and
possibly other DPs, as determined by the Board.
</p>
<p>
In the past, expenses which PGLAF incurred on behalf of DP have included
registration of domain names, buying high-speed scanner set-ups, buying
our server, and paying for the hosting of our server. Hosting costs have
been paid by PGLAF for this year (through Feb, 2007 I believe). DPF
will, of course, be assuming responsibility for our future expenses. DPF
has requested that PGLAF transfer ownership of the DP domain names to DPF.
</p>
<p>
It is unlikely that DP will have any paid staff in the foreseeable
future. Some software development may be contracted out, as necessary.
Funds raised by DPF will be used to support the website, recruit new
volunteers, improve the custom software, buy additional hardware as
needed, and for such other items as the Board determines are
appropriate.
</p>

