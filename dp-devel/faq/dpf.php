<h2>History:</h2>

<p>
When Distributed Proofreaders (DP) was founded by Charles Franks in 2000
it was just a website hosted on his home computer. He accepted
contributions towards the operating costs of the site, but they were not
tax-deductible. Sometime around July 2002, he decided to officially
associate the website with Project Gutenberg, so that PGLAF (Project
Gutenberg Literary Archive Foundation), which already existed as a
non-profit, tax-deductible donation-accepting organization, could deal
with the financial matters. Charles turned over the money that had been
donated to DP to PGLAF and from that time, PGDP operated under the
financial umbrella and corporate protection of PGLAF. 
</p>
<p>
Since then, DP has grown enough that it makes sense for us to become our
own legal identity and have financial independence. The
incorporation documents and by-laws have been written to be as generic
as possible, allowing great flexibility on the part of the Board. This
will allow the Foundation the maximum opportunity to grow and change as
time and circumstances dictate. 
</p>
<p>
As indicated by the presence of Greg Newby (CEO of PGLAF) as a Trustee, the
establishment of DPF does not imply any change in DP's support of, or
alignment with, PG's mission. DPF simply formalizes what has been true
all along, that DP is a separate organization from PG. 
</p>

