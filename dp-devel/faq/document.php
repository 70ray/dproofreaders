<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="poetry_i">Poetry/Epigram Indentation</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;"><font size="3">For poetry, use
2 spaces for indenting it. Do not try to center the text; leave it aligned
on the left margin. Try to follow the format of the original if some lines are indented more than others,
except for the start of paragraphs, which should be aligned on the left margin.</font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="letter">Letter Indentation</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Leave letters unindented, like you would for <a href="#para_space">Paragraph Spacing/Indentation</a>.

<p align="center" style="margin-bottom: 0cm;"><a name="tables">Tables</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Tables should be formatted with spaces to look approximately like the original table.
The width of a table should not go beyond 70 characters and should be surrounded with /* */ like the Poetry Marking example above.
</p>
<br>
<p align="center" style="margin-bottom: 0cm;"><a name="double_q">Double Quotes</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Single spacing around quote marks
and punction is the correct formatting for the text. Please try to just use the " double quote,
since the directional double quotes will have to be replaced to make it
ASCII compliant. Do not change single quotes to double quotes. See the Chapter Headers image and text for an example.</p>
<br>
<p align="center" style="margin-bottom: 0cm;"><a name="single_q">Single Quotes</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>

</p>
<p align="left" style="margin-bottom: 0cm;">Single quotes are used for a variety of purposes.
They sometimes are used throughout where current usage would be double quotes and they should be left alone.
The are also used for slang of words or shortened version. Try to put them how the image has them.
Last of all, please try to just use the <tt>'</tt> single quote to make it easier for everyone.</p>
<br>
<br>
<p align="center" style="margin-bottom: 0cm;"><a name="period_s">Periods Between Sentences</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Single space after periods (vice
two spaces). But don't bother to remove double spaces after periods if they're already
in the scanned text -- we will do that automatically in post-processing.
See the Chapter Headers image and text for an example.
</p>
<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="lb">Line Breaks</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>

</p>
<p align="left" style="margin-bottom: 0cm;"><font size="3">Leave all line
breaks in so that the next proofreader can compare the text easily. Extra blank lines
that are not in the image should not be included. See the
Chapter Headers image and text for an example.</font></p>
<p style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="ln">Line Numbers</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Remove all line numbers that exist in
the text. Since the text will be reformatted later, they will not be helpful.</p>
<p style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><a name="extra_s">Extra Spacing/Stars/Line Between Paragraphs</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>

<p align="left" style="margin-bottom: 0cm;">Some pages will have extra spacing between
paragraphs due to "thought breaks", change in scene, or a bit of suspense. These are
intended by the author, so we preserve them by putting a blank line, 5 *'s indented 7
spaces and then 7 spaces apart so:<br>
<tt><br>end of text.<br>
<br>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;*&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;*
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;*&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;*
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;*<br>
<br>
Text again.</tt><br>
<p>See the <a href="#page_hf">Page Headers/Footers</a> image/text for an example.</p>

<p align="center" style="margin-bottom: 0cm;"><a name="period_p">Period Pause "..."</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Leave a space before the three dots, and a space after.
The exception is at the end of a sentence, when there would be no space, four dots, and a space after.
For example:<br>
<br><tt>This is the end.... That I know ... is true.</tt></p>

<p align="center" style="margin-bottom: 0cm;"><a name="f_chars">Foreign/Non-ASCII Characters</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;"><font size="3">Try to put in the
proper <b>foreign characters</b>.
<br>
But there is a problem with using accented characters -- they are upper ASCII,
or characters above code 127. This can cause display problems for any computer
not using the Western European character set (MS Windows codepage 1252 /
ISO 8859-1 or -15). Furthermore, Apple never accepted ISO 8859 and some characters
don't match up (a good example is the curly open quote (0147), which Apple
maps to an accented capital O). Use with caution.
</font>
</p>

<p align="left" style="margin-bottom: 0cm;">
<b>For Windows</b>, there are several ways to get these characters:
<ul compact>
  <li>you can use the character map program
     (Start: Run: charmap) to select an individual letter, and then cut & paste.
  <li>if using the enhanced proofing interface, the
      <span style="font-style: italic;">more</span>
      tag creates a pop-up window
      containing these characters, which you can then cut & paste.
  <li>or you can type the Alt+NumberPad shortcut codes for these characters.
      <br>(They're a lot faster than using cut &amp; paste,
          once you get used to the codes.)
      <br>You hold the Alt key and type the four digits on the
          <span style="font-style: italic;">Number Pad</span> -- the number row over
          the letters won't work.
      <br>And you must type the leading 0.
          Note that the capital version of a letter is 32 less than the lower case.
</ul>
