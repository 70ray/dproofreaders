<p align="center" style="margin-bottom: 0cm;"><a name="blank_page">Blank Page</a></p>

<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Blank pages may be in the project you are working on due to the original book having a blank page and us trying to cut down on the work needed to find the page, remove it, and renumbering the remaining pages. Please put in the text box <tt>[Blank Page]</tt>
if both the text box and the image are blank. If only one of them is blank,
follow the directions for a Bad Image or Bad Text.</p>
