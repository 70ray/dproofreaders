<p align="center" style="margin-bottom: 0cm;"><a name="contract">Contractions</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Remove any extra space in contractions,
e.g.  would n't should be wouldn't</p>

<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="poetry_m">Poetry Marking</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;"><font size="3">Please place a
<tt>/*</tt> at the start of the poetry and a <tt>*/</tt> at the end of the poetry. This will
help the post-processor find the poetry quicker to see what not to reformat. If it starts
at the beginning of the page, put a <tt>*</tt> before the <tt>/*</tt> and if it is at the
end of the page, put a <tt>*</tt> after the <tt>*/</tt>.</font></p>

