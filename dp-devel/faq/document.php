<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="footnotes">Footnotes/Endnotes</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">
Replace the footnote number, *, dagger, etc. that marks a footnote location with the
actual text of the footnote, surrounded by <tt>[Footnote: ]</tt>.  That is, footnotes
should be be moved from the bottom of the page to the place on the page where they
are referenced in the text.
If a footnote is a continuation of a  footnote on a previous page, move it to the top of
the page, surround it with <tt>[Footnote: ]</tt>, and put a * before it. If a
footnote continues on the next page, it should be moved to where it is referenced on the
page, surrounded by <tt>[Footnote: ]</tt>, and marked with a * at the end of it. If a
footnote/endnote is referenced in the text but does not appear on that page, surround
the footnote/endnote marker with the <tt>[Footnote: ]</tt> and put a * next to it. See
the <a href="#page_hf">Page Headers/Footers</a> image/text for an example footnote.</p>

<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="italics">Italics</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;"><font size="3">Every portion
that is italicized should have a &lt;i&gt; inserted at the start and a &lt;/i&gt;
inserted at the end of the italics. If it is at the start of a page, place
a * in front of the &lt;i&gt; so that the post-proofreader can see if there
is more on the previous page. If it is at the end of a page, place a * after
the &lt;/i&gt; so that the post-proofreader can see if there is more on the
next page. Punctuation goes OUTSIDE the italics, unless it is an entire
sentence or section that is being italicized. That is not a big deal,
but is an answer for those curious.
See the Illustration image for an example of how to do the italics.</font></p>

<p align="left" style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><a name="bold">Bold Text</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Capitalize bold text. If it's the first word of a chapter and it's in bold, capitalize it. See the Page Header/Footers image/text for an example.</p>
<p align="left" style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><a name="eol_hyphen">End-of-line Hyphenation</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Remove the end-of-line hyphenation,
unless it really is a hyphenated word like well-meaning. Please join the two
halves of a previously hyphenated word. See the Chapter Headers image for
two examples of each kinds (nar-row turns into narrow, but low-lying keeps
the hyphen). Words like to-day and to-morrow that we don't commonly hyphenate
were often hyphenated in the books we are working on.  In those cases, leave it
hyphenated the way the author did.
<br>
<br>

</p>
<p align="center" style="margin-bottom: 0cm;"><a name="eop_hyphen">End-of-page Hyphenation</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Mark the end-of-line hyphenation
with a * after the hyphen. On pages that start with part of a word from a
previous page, place a * before the word.</p>
<p align="left" style="margin-bottom: 0cm;"><br>
</p>
<p align="center" style="margin-bottom: 0cm;"><a name="em-dashes">EM-Dashes</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Please use two minus signs with
no space on either side for the em-dash. For longer em-dashes, use 4 signs.  There are a
few occasions that rule is broken, such as when it is being used instead of a name or
number, as in Mr B---- or -- Charing Rd, where it is appropriate to leave the spaces.
If an em-dash appears at the start or end of your OCR'd text, join it with the other
line so that there is no space or line breaks around it.  Only if the author started or
ended his paragraph in the text with an em-dash should you leave it at the start or end
of a line.
</p>
<p align="left" style="margin-bottom: 0cm;"><br>
</p>

