<p align="center" style="margin-bottom: 0cm;"><a name="para_side">Paragraph Side-Descriptions</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Some books will have descriptions
of the paragraph along the side of the text. These "Sidenotes" need to be moved above
the paragraph that they belong to, surrounded with <tt>*[Sidenote: the-text]</tt>.
If the paragraph began on a previous page, put the
Sidenote at the top of the page and mark if with *  so that the post-proofreader can
see that it belongs on the previous page.
Like this: <tt>*[Sidenote: the-text]</tt>.
<br>
