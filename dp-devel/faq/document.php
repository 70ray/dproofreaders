<br>
<p align="center" style="margin-bottom: 0cm;"><font size="3"><a name="illust">Illustrations</a></font></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;"><font size="3">Text for an illustration
should be surrounded by <tt>[Illustration: ]</tt>. If there is no text, just place <tt>[Illustration]</tt> there.
If it is in the middle of a paragraph or on the side
of a paragraph, move the <tt>[Illustration: ]</tt> to either above or below the paragraph, based on where you can put it without
it being in the middle of a paragraph.</font></p>

