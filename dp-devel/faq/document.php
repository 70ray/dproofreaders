<p align="center" style="margin-bottom: 0cm;"><a name="chap_head">Chapter Headers</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">Chapter headers need to be left
in the text. A chapter header will start a bit farther down the page and won't have the
page numbers.  Old books often printed the first word of the chapter in all caps; change
this to a normal word (first letter only capitalized), unless it's bold text. Watch out
for a missing double quote at the start of the first paragraph, which some publishers
did not include or which the OCR missed due to a large capital in the original. A blank
line is needed between the "CHAPTER XXX" and the chapter description.</p>

