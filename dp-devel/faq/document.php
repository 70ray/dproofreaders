<p align="left" style="margin-bottom: 0cm;">
<font size="3">
This document is written in order to resolve formatting differences done by proofreaders
on a variety of items; that is, so we all do formatting the same way.  This will make
the post-proofreader&#39;s work easier and will increase the quality of the books that
are created through this site.
