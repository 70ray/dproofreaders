<p align="center" style="margin-bottom: 0cm;"><a name="bad_image">Bad Images</a></p>
<p align="center" style="margin-bottom: 0cm;"><br>
</p>
<p align="left" style="margin-bottom: 0cm;">If an image is bad (not loading,
chopped off, unable to be read), please put a post about this bad image in the project
comments forum.  Do not click on "Quit without Saving"; if you do, the next person will
get that page. Click on your back button to the project listing page or go back to
<a href="../tools/proofers/proof_per.php">your Personal Page</a>
until we create a better
method in the coming weeks.</p>
<p align="left" style="margin-bottom: 0cm;">
It's fairly common for the image to be good, but the OCR scan is missing the first line
or two of the text.  Please just type in the missing line(s).
If nearly all of the lines are missing in the scan, then either type in the whole page
(if you are willing to do that), or just click on the "Quit without Saving" button and
the page will be reissued to someone else.  If there are several pages like this, you
might post a note in the project comments forum to notify the project manager.
</p>
