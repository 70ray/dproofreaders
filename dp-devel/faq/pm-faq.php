<a name="6"><b>6. How do I process the output from the site to make a PG e-text?</b></a>
    <blockquote>
    I generally make a 'working' copy of the file and keep the original for cross-referencing text to the original file. If you have questions
    you can search the 'original' copy for the suspect text, find out what file it was in and review the corresponding image.
    <p>
    Removing the page separators and correctly joining the pages in the 'working' copy is where PRTK becomes invaluable as this process is semi-automatic.
    <p>
    Use GutCheck (PRTK has a front end for GutCheck) and correct what it finds. Pay special attention to items marked with an asterisk.
    <p>
    Next, I generally follow this process in MS WORD:

    <pre>
    - replace ^p^p with @@
    - replace ^p with a space
    - replace @@ with ^p^p
    - replace double spaces with single spaces (cleans up extra whitespace)
    - Fix em-dashes
         -- replace blah --blah with blah--blah
            likewise blah - blah and blah-- blah
    - search for spaced punctuation
         -- blah " blah  blah ? blah blah ! blah etc.
    - replace -- with @@
    - replace common single space words with @ (depends on length of text as this
      saves time on really large texts)
         -- to-day should be to@day, etc.
         -- go through each remaining - and see if it should be -- (hyphens that
            should be em-dashes)
    - spell check
    - search for * and fix
    - seach for _ and make into capitals
         -- except for _I_
    - select all text
         --set font to 'courier new' and size 10
         --set right margin to around 65-70 characters.
           A right margin of 2 inches is about right.
    - do 'save as text with line breaks'
    - go through and properly space/indent poetry, songs etc.
    - Add blank lines (5 before and 1 after) chapter/section headings
    </pre>
    <p>
    Robert has a Word macro that automates part of the above. PRTK has a "Dask Check" tool to assist with the correction of em-dashes and formatter tool to assist with capitalization, indention, etc.
    <p>

    </blockquote>
