<a name="5"><b>5. How do I get the 'finished product' off the DPR website? </b></a>
    <blockquote>
    After all the pages have gone through both rounds of proofing there is a 'concatenate' link, once you click on this you are
    taken to a page that asks you what you want to name the resultant file and whether or not you want page separators between
    each page. The page separators contain the filename of the text file and are very handy in the final proofing/assembly stage.
    </blockquote>
