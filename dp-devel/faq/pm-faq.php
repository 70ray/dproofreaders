<a name="1"><b>1. What are the steps in obtaining/processing books through Distributed Proofreaders?</b></a>
<p>
<blockquote>

Several of these steps mention using Robert's Proofreaders Toolkit (PRTK). PRTK is a tool written specifically for PM's and we use it to streamline many common tasks. It is availible for Windows only and you can get the latest copy at <a href="http://robertrowe.dns2go.com">Robert Rowe's Website.</a> For those without access to a computer running Windows, feel free to email <a href="mailto:robert_rowe@yahoo.com">Robert Rowe</a> for assistance. He has volunteered to run your project through PRTK for you.<br><br>

<b>A. Find an elgible book.</b><br>
    <blockquote>
    Basic eligibility: copyrighted 1922 or earlier. <br><br>
    Possible sources include: Flea Market, Yard Sale, Estate Auctions, Online Auctions, Local Library, etc. Libraries usually don't make good sources because most won't let you check out their old books. Also, the scanning process is a little rough on the book and it may get damaged.
    </blockquote>

<b>B. Ensure the book is not in the PG Archive or on David Price's <a href="http://www.dprice48.freeserve.co.uk/GutIP.html">In-Progress List</a>.</b><br>
    <blockquote>
    It is better to check the In-progress list than searching PG. David's list includes projects already started by other volunteers. If you find your book listed but the clearance date is over a year old then it is probably OK to go ahead and do.
    </blockquote>

<b>C. Buy/borrow the book.</b><p>

<b>D. Send copies of the Title Page and Verso (TP&V) to Michael Hart for clearance.</b><br>
    <blockquote>
    As of January, 2002, there are two ways to submit a text for clearance.
    <p>
    <strong>To submit by paper mail,</strong> photocopy the front and back
    of the title page, even if the back is blank, write your e-mail address
    on it, and send the photocopies to:<br><br>

    <ul>

    MICHAEL STERN HART<br>
    405 WEST ELM STREET<br>
    URBANA, IL 61801-3231 USA<br>

    </ul>

    <p>
    This is called Title Page &amp; Verso, or TP&amp;V for short, and is needed
    for copyright research. A colored envelope is best, to make sure your letter
    is easily recognized as TP&amp;V.</p>
    <p>
    E-mail Michael <a href="mailto:hart@pobox.com">hart@pobox.com</a> when you
    send them, so he knows they're on the way. It's a good idea to check back
    with him by e-mail after a week or so if you haven't heard from him.</p>
    <p>
    <strong>To submit by e-mail,</strong> scan the front and back of
    the title page, even if the back is blank, make sure that the
    print is legible, and e-mail the images to
    <a href="mailto:gbnewby@ils.unc.edu">Greg Newby</a>
    as TIFF, JPEG or GIF in medium resolution.

    <p>
    Whichever method you use, you should expect to get an e-mail back after
    about a week, with one line containing the Author, Title, your name
    and date with the word "OK" at the end. This means that your text has been cleared.
    If you don't get any response, e-mail to check that your TP&amp;V was
    received OK. If the word at the end of the line is not "OK", then your
    text is not eligible.
    <p>

    </blockquote>
<b>E. Start scanning (once the clearance is obtained).</b><br>
    <blockquote>
    You should set your scanner to "Line Art" or "Black and White" mode. Your resolution should be 300 X 300. If the book is very small or if it has very small print you might want to try a resolution of 400 X 400. A higher resolution will just increase the file size without much (if any) improvement in image quality. (Except on very small print). To speed up the scanning process scan 'two-up' images (two book pages per image). To help with the next step, processing into 'one-up' images, try to keep the book in the same place on the scanner for each scan, say tight into a corner.
    </blockquote>
<b>F. Process the book pages into 'one-up' (one book page per image) files.</b><br>
    <blockquote>
    Generally there are two 'easy' ways to get 'one-up' images from 'two-up' images:
<p> 1.) If your scanning program has an option to automatically split images as they are scanned, set this option (See <a href="#8">Scanning/OCR programs</a>). I don't really recommend this as the output can be inconsistent.

<p>2.) Use PRTK (See PRTK Help) to split images containing 2 pages. If you keep the book in the same place on the scanner for each scan PRTK can easily split the images.

<p>
If your scanner has an ADF (Automatic Document Feeder) you may wish to cut the pages from the spine and feed them through your ADF. In addition, you will probably want to clean and crop your images. PRTK has several tools to assist with this process.
    </blockquote>
<b>G. OCR image files into text files (one text file per image).</b><br>
    <blockquote>
    If your OCR program has an option to insert a blank line between paragraphs then use this option. It will cut way down on this kind of mistake and the proofers will appreciate it. If your program does not have this option, PRTK has a tool to assist you with inserting this line. If you are using Finreader for your scanning/OCR, Robert has a set of saved settings that you may find useful.
    </blockquote>
<b>H. Automatically Pre-Proof pages using PRTK.</b><br>
    <blockquote>
    PRTK has a pre-proofing tool designed to detect and correct many common mistakes left by OCR programs. (See PRTK Help)
    </blockquote>
<b>I. Create the project on the DPR website.</b><br>
    <blockquote>
    On the main PM page (at the bottom) is a link to the "Create Project" page. After you create your project, it will be listed on your PM page. Each project has an "Edit Comments" link. You can email Robert or myself for a file containing the "standard" comments or make up anything you like. HTML is allowed in the comments including links to example pictures.
    </blockquote>
<b>J. Convert your images to PNG.</b><br>
    <blockquote>
    Browsers are limited to certain types of image files. We have found that the PNG file type best suits our needs.
    </blockquote>
<b>K. Load images and text files onto the website.</b><br>
    <blockquote>
    You have 3 ways to upload your text and images.<br><br>

    1. Use the "Upload a master file to this project" link on the project page.<br>
    2. Use Robert's Project Uploader. (contact Robert for the latest version)<br>
    3. FTP the files to Robert or myself and we'll bulk upload them for you.<br>
    </blockquote>
<b>L. Release the project for proofing.</b><br>
    <blockquote>
    This is done by toggling the "Availibility" of the project on the PM's page. Please coordinate your release with the other PM's. We all take turns and try to keep an even mix of books without having too many/few availible.
    </blockquote>
<b>M. Shepherd the project through the site. (See <a href="#4">Shepherding a Project</a>)</b><p>

<b>N. Download the output from the site and massage it into a postable e-text. (PRTK helps immensely here)</b><p>
    <blockquote>
    The Project Status page has a link for this step. The site will stich your files together (with a file separator) and provide a download link. PRTK has a Post-Processing tool designed to read this file and assist in making the final formatting adjustments.
    </blockquote>
<b>O. Send the e-text in for posting to the PG archive.</b><br>
    <blockquote>
    Most PM's email me their completed etext for a final double check and submission. If you send me you etext, please include your "Raw" file (straight from the site) also as this helps tremendously.
    </blockquote>
</blockquote>

